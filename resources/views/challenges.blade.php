educational system. One of those changes is establishing a new parent-teacher
communication system.
<br/>
<br/>
Through regular communication with parents, teachers can share information about a
student’s good work and achievements. If a student begins to struggle with a certain subject
or exhibits behavioral problems, the parent will already have a relationship with the teacher,
which helps facilitate parent-teacher collaboration. This cooperative relationship empowers
parents to step in and support the student at home, supplementing what the teacher is doing at
school.
